#include <iostream>

using namespace std;


int main() {
  int a = 0, b = 0;

  cout << "Enter two integers: ";
  cin >> a >> b;

  cout << "\nsum = " << a + b << endl;
  cout << "difference =  " << a - b << endl;
  cout << "product = " << a * b << endl;
  cout << "quotient = " << a / b << endl;
  return 0;

}